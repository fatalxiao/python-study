print('1 + 1 =', 1 + 1)
print('2 - 2 =', 2 - 2)
print('3 * 3 =', 3 * 3)
print('4 / 4 =', 4 / 4)  # -> float
print('0 / 4 =', 0 / 4)  # -> float

# 商向下取整
print('5 // 2 =', 5 // 2)
print('5.0 // 2 =', 5.0 // 2)  # -> float

# 取模
print('5 % 2 =', 5 % 2)
print('5.0 % 2 =', 5.0 % 2)  # -> float

# 幂
print('5 ** 2 =', 5 ** 2)
print('5.0 ** 2 =', 5.0 ** 2)  # -> float

# 混合
print('5 ** 2 + 5 / 2=', 5 ** 2 + 5 / 2)  # -> float

a = 1
print('a =', a)
a += 1
print('a =', a)
a -= 1
print('a =', a)
